public class DetalleVenta {
    
    String cantidadVenta, productoVenta, precioVenta, importeVenta;

    public String getCantidadVenta() {
        return cantidadVenta;
    }

    public void setCantidadVenta(String cantidadVenta) {
        this.cantidadVenta = cantidadVenta;
    }

    public String getProductoVenta() {
        return productoVenta;
    }

    public void setProductoVenta(String productoVenta) {
        this.productoVenta = productoVenta;
    }

    public String getPrecioVenta() {
        return precioVenta;
    }

    public void setPrecioVenta(String precioVenta) {
        this.precioVenta = precioVenta;
    }

    public String getImporteVenta() {
        return importeVenta;
    }

    public void setImporteVenta(String importeVenta) {
        this.importeVenta = importeVenta;
    }
    
    
}
